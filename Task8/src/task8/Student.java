public class Student {

    String name;
    String ID;
    String address;
    double cgpa;

    public String getName() {
        return name;
    }

    public void setName(String n) {
        this.name = n;
    }

    public String getID() {
        return ID;
    }

    public void setID(String i) {
        this.ID = i;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String a) {
        this.address = a;
    }

    public double getCgpa() {
        return cgpa;
    }

    public void setCgpa(double c) {
        this.cgpa = c;
    }
}
